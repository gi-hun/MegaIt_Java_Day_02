/*
 * # 영수증 출력[1단계]
 * 1. 메뉴판을 출력한다.
 * 2. 사용자는 주문하고자 하는 메뉴의 번호를 입력한다.
 * 3. 현금을 입력받는다.
 * 4. 입력받은 현금과 메뉴가격을 확인해, 영수증을 출력한다.
 * 5. 단, 현금이 부족한 경우 "현금이 부족합니다."라는 메세지를 출력한다.
 */
//12:16-12:24
package day_02;

import java.util.Scanner;

public class IfEx11 {
	public static void main(String[] args) {
		int price1 = 8700;
		int price2 = 6200;
		int price3 = 1500;
		
		System.out.println("=== 롯데리아 ===");
		System.out.println("1.불고기 버거 : " + price1 + "원");
		System.out.println("2.새우    버거 : " + price2 + "원");
		System.out.println("3.콜         라 : " + price3 + "원");
		System.out.println("");
		
		Scanner sc = new Scanner(System.in);
		System.out.println("메뉴를 선택해주세요");
		int select = sc.nextInt();
		
		if(select == 1) {
			System.out.println("현금을 입력해주세요 ");
			int cash = sc.nextInt();
			System.out.println("==영수증==");
			System.out.println("불고기버거");
			System.out.println("잔돈:"+ (cash-price1));
			if((cash < price1) || (cash < 0))
				System.out.println("현금이 부족합니다.");
		}
		else if(select == 2) {
			System.out.println("현금을 입력해주세요 ");
			int cash = sc.nextInt();
			System.out.println("==영수증==");
			System.out.println("새우 버거");
			System.out.println("잔돈:"+ (cash-price2));
			if((cash < price2) || (cash < 0))
				System.out.println("현금이 부족합니다.");
		}
		else if(select == 3) {
			System.out.println("현금을 입력해주세요 ");
			int cash = sc.nextInt();
			System.out.println("==영수증==");
			System.out.println("콜    라");
			System.out.println("잔돈:"+ (cash-price3));
			if((cash < price3) || (cash < 0))
				System.out.println("현금이 부족합니다.");
		}
	}
}
